     * Handle the Product "updated" event.
     *
     * @param  \App\Models\Product  $product
     * @return void
     */
    public function updated(Product $product)
    {
        if ($product->quantity == 0 && $product->isAvailable()) {
            $product->status = Product::UNAVAILABLE_PRODUCT;
            $product->save();
        }
    }
